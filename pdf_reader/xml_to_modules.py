def print_xml_texts(xml_string):
    root = ET.fromstring(xml_string)
    texts = []

    def recurse(node, parent_attrib=None):
        # Start by inheriting parent's attribs
        attrib = dict(parent_attrib) if parent_attrib else {}
        # Update with this node's attribs (if any, possibly overwriting parent)
        attrib.update(node.attrib)
        # Add main text if present
        if node.text and node.text.strip():
            row = {
                "left": attrib.get("left"),
                "font": attrib.get("font"),
                "bold": node.tag == "b",
                "text": node.text.strip()
            }
            texts.append(row)
        # Recurse through children, always passing along inherited attribs
        for child in node:
            recurse(child, attrib)
        # (Tail text usually not positional; skip or handle if you wish)

    recurse(root)
    print(tabulate(texts, headers="keys", tablefmt="grid"))

